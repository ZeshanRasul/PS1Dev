    ; Drawing Area Top Left
    li $t1, 0xE3000000
    sw $t1, GP0($a0)

    ; Drawing Area Bottom Right
    li $t1, 0xE403BD3F
    sw $t1, GP0($a0)

    ; Drawing Offset
    li $t1, 0xE5000000
    sw $t1, GP0($a0)

