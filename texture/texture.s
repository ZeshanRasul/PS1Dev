    sw $t1, GP0($a0)
    li $t1, 0x00A00050
    sw $t1, GP0($a0)
    li $t1, 0x00A000F0
    sw $t1, GP0($a0)
    li $t1, 0x00F00050
    sw $t1, GP0($a0)
    li $t1, 0x00F000F0
    sw $t1, GP0($a0)

    ;;; Copy texture data from CPU to GPU
    li $t1, 0xA0000000
    sw $t1, GP0($a0)

    li $t1, 0x00640096
    sw $t1, GP0($a0)

    li $t1, 0x00200020
    sw $t1, GP0($a0)

    li $t0, IMG_SIZE_IN_BYTES
    srl $t0, $t0, 2

    la $t2, Image

TextureTransfer:
    lw $t1, 0($t2)
    nop
    sw $t1, GP0($a0)
    addiu $t2, 4
    addiu $t0, -1
    bnez $t0, TextureTransfer
    nop

LoopForever:
    j LoopForever
    nop

